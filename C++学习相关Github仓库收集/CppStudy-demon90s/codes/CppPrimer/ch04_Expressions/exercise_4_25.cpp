/*
 * 练习4.25：如果一台机器上int占32位，用的是Latin-1字符集，其中字符'q'的二进制形式是01110001，那么
 * 表达式~'q' << 6的值是什么？
 */

#include <iostream>

using std::cout;
using std::endl;
using std::cin;

int main()
{
	// 先把'q'提升为int，然后对其取反，再左移6位
	// 0000 0000 0000 0000 0000 0000 0111 0001
	// 1111 1111 1111 1111 1111 1111 1000 1110
	// 1111 1111 1111 1111 1110 0011 1000 0000
	// 输出结果是：-7296
	cout << (~'q' << 6) << endl;

	cout << (int)0xFFFFE380 << endl;

	return 0;
}
